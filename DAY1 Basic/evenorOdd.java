import java.util.*;
public class evenorOdd {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int num=sc.nextInt();
        if(num%2==0){
            System.out.println("this number is Even");
        }
        else{
            System.out.println("this number is Odd");
        }
        
    }
    
}
